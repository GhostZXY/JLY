package com.jly.bean;

public class Address {
	private int a_id;		  //地址id
	private String a_address; //联系电话
	private String a_tel;	  //电话
	private String a_name;	  //地址名称
	private String u_id;	 //用户id
	private String a_isdefault;//详细地址
	public int getA_id() {
		return a_id;
	}
	public void setA_id(int a_id) {
		this.a_id = a_id;
	}
	public String getA_address() {
		return a_address;
	}
	public void setA_address(String a_address) {
		this.a_address = a_address;
	}
	public String getA_tel() {
		return a_tel;
	}
	public void setA_tel(String a_tel) {
		this.a_tel = a_tel;
	}
	public String getA_name() {
		return a_name;
	}
	public void setA_name(String a_name) {
		this.a_name = a_name;
	}
	
	public String getU_id() {
		return u_id;
	}
	public void setU_id(String u_id) {
		this.u_id = u_id;
	}
	public String getA_isdefault() {
		return a_isdefault;
	}
	public void setA_isdefault(String a_isdefault) {
		this.a_isdefault = a_isdefault;
	}
	
	
	
}
